--그룹함수 : 여러행에 대하여 집계출력
--AVG, SUM, MIN, MAX, COUNT
SELECT AVG(SALARY), SUM(SALARY), MIN(SALARY),
       MAX(SALARY), COUNT(SALARY)      
FROM EMPLOYEES;

SELECT MIN(HIRE_DATE), MAX(HIRE_DATE) FROM EMPLOYEES;
SELECT MIN(FIRST_NAME), MAX(FIRST_NAME) FROM EMPLOYEES;

--COUNT(컬럼) : NULL이 아닌 데이터개수
--COUNT(*) : 전체행의 개수
SELECT COUNT(FIRST_NAME) FROM EMPLOYEES;
SELECT COUNT(DEPARTMENT_ID) FROM EMPLOYEES;
SELECT COUNT(COMMISSION_PCT) FROM EMPLOYEES;
SELECT COUNT (*) FROM EMPLOYEES;

-----주의할 점-----
--그룹함수 : 그룹함수와 일반컬럼을 동시에 출력할 수 없습니다 (오라클만)
SELECT FIRST_NAME, SUM(SALARY) FROM EMPLOYEES;

